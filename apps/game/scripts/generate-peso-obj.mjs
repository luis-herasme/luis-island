/**
 * Generates public/peso.obj: a coin whose faces are textured by peso.jpg,
 * an atlas with the coin's front on the left half and its back on the right
 * (see public/peso-front.jpeg / peso-back.jpeg, composited side by side).
 *
 * The coin stands upright: a disc of SEGMENT_COUNT segments in the XY plane
 * facing +Z (front) and -Z (back), joined by a rim strip. Face UVs inscribe
 * a circle in each atlas half — the back mirrored horizontally so it reads
 * correctly when seen from behind. The rim samples one plain-gold texel.
 *
 * Run from the repo root: node apps/game/scripts/generate-peso-obj.mjs
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const SEGMENT_COUNT = 64;
const RADIUS = 0.5;
const HALF_THICKNESS = 0.04;

/**
 * The scans have a dark edge ring around the coin; shrinking the face UV
 * circle keeps the sampled area inside the gold.
 */
const FACE_UV_SHRINK = 0.955;

// A plain gold spot on the front face of the atlas, sampled by the rim.
const RIM_U = 350 / 3000;
const RIM_V = 0.5;

const positionLines = [];
const uvLines = [];
const normalLines = [];
const faceLines = [];

/** Each add returns the 1-based OBJ index of the added element. */
const addPosition = (x, y, z) => positionLines.push(`v ${format(x)} ${format(y)} ${format(z)}`);
const addUV = (u, v) => uvLines.push(`vt ${format(u)} ${format(v)}`);
const addNormal = (x, y, z) => normalLines.push(`vn ${format(x)} ${format(y)} ${format(z)}`);
const addTriangle = (a, b, c) => faceLines.push(`f ${a.join("/")} ${b.join("/")} ${c.join("/")}`);

function format(value) {
  return Number(value.toFixed(6)).toString();
}

const frontNormal = addNormal(0, 0, 1);
const backNormal = addNormal(0, 0, -1);

const frontCenterPosition = addPosition(0, 0, HALF_THICKNESS);
const backCenterPosition = addPosition(0, 0, -HALF_THICKNESS);
const frontCenterUV = addUV(0.25, 0.5);
const backCenterUV = addUV(0.75, 0.5);
const rimUV = addUV(RIM_U, RIM_V);

const frontRing = [];
const backRing = [];
const frontRingUV = [];
const backRingUV = [];
const rimNormals = [];

for (let segment = 0; segment < SEGMENT_COUNT; segment++) {
  const angle = (segment / SEGMENT_COUNT) * 2 * Math.PI;
  const cosine = Math.cos(angle);
  const sine = Math.sin(angle);

  frontRing.push(addPosition(RADIUS * cosine, RADIUS * sine, HALF_THICKNESS));
  backRing.push(addPosition(RADIUS * cosine, RADIUS * sine, -HALF_THICKNESS));

  // Each atlas half is square, so a radius of 0.25 in u and 0.5 in v traces
  // a circle in pixels. The back mirrors u to read correctly from behind.
  frontRingUV.push(addUV(0.25 + 0.25 * FACE_UV_SHRINK * cosine, 0.5 + 0.5 * FACE_UV_SHRINK * sine));
  backRingUV.push(addUV(0.75 - 0.25 * FACE_UV_SHRINK * cosine, 0.5 + 0.5 * FACE_UV_SHRINK * sine));

  rimNormals.push(addNormal(cosine, sine, 0));
}

for (let segment = 0; segment < SEGMENT_COUNT; segment++) {
  const next = (segment + 1) % SEGMENT_COUNT;

  addTriangle(
    [frontCenterPosition, frontCenterUV, frontNormal],
    [frontRing[segment], frontRingUV[segment], frontNormal],
    [frontRing[next], frontRingUV[next], frontNormal],
  );

  addTriangle(
    [backCenterPosition, backCenterUV, backNormal],
    [backRing[next], backRingUV[next], backNormal],
    [backRing[segment], backRingUV[segment], backNormal],
  );

  // The rim reuses the ring positions with its own radial normals.
  addTriangle(
    [frontRing[segment], rimUV, rimNormals[segment]],
    [backRing[segment], rimUV, rimNormals[segment]],
    [backRing[next], rimUV, rimNormals[next]],
  );
  addTriangle(
    [frontRing[segment], rimUV, rimNormals[segment]],
    [backRing[next], rimUV, rimNormals[next]],
    [frontRing[next], rimUV, rimNormals[next]],
  );
}

const obj = ["# Dominican one peso coin — generated by generate-peso-obj.mjs", ...positionLines, ...uvLines, ...normalLines, ...faceLines, ""].join("\n");

const outputPath = join(dirname(fileURLToPath(import.meta.url)), "../public/peso.obj");
writeFileSync(outputPath, obj);
console.log(`Wrote ${outputPath}: ${positionLines.length} positions, ${faceLines.length} triangles`);
